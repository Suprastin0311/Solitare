package org.ui;

/**
 * Класс {@code App} представляет основной входной пункт для модуля 
 * "interface". Этот класс запускает выполнение программы.
 */
public class App 
{
	
	/**
     * Метод {@code main} — это точка входа в приложение "interface".
     * Он выводит сообщение на консоль для подтверждения запуска модуля.
     * 
     * @param args массив строковых аргументов командной строки
     */
    public static void main( String[] args )
    {
        System.out.println( "Hello World!" );
    }
}
